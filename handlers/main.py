        "üî¥ –í–∏–±–∞—á—Ç–µ, –∞–ª–µ –º–µ–Ω—ñ –ø–æ—Ç—Ä—ñ–±–Ω–µ —Ç—ñ–ª—å–∫–µ\n–ø–æ—Å–∏–ª–∞–Ω–Ω—è –Ω–∞ —Å—Ç–æ—Ä—ñ–Ω–∫—É olx.ua –∑ –Ω–µ—Ä—É—Ö–æ–º—ñ—Å—Ç—é.\n—Ç–∞–∫–æ–≥–æ —Ç–∏–ø—É https://www.olx.ua/....",
        disable_web_page_preview=True,
