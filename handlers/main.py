async def main(message: types.Message):
    try:
        await get_data(message)
    except:
        await message.answer(
            "–í–∏–Ω–∏–∫–ª–∞ –ø–æ–º–∏–ª–∫–∞ ‚ùå\n–°—Ç–æ—Ä—ñ–Ω–∫—É –Ω–µ –±—É–ª–æ –∑–∞–ø–∞—Ä—Å–µ–Ω–æ",
            reply_markup=types.ReplyKeyboardRemove(),
        )


